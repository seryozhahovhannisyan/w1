    public static CurrencyType getDefault() {
        return USD;
    }

    public static synchronized CurrencyType valueOf(final int value) {
        for (CurrencyType e : CurrencyType.values()) {
            if (e.id == value) {
                return e;
            }
        }
        return null;
    }

    public static CurrencyType codeOf(final String code) {
        for (CurrencyType e : CurrencyType.values()) {
            if (e.getCode().equalsIgnoreCase(code)) {
                return e;
            }
        }
        return getDefault();
    }

